export const getCategoryCount = (category: string) => {
  if (category === '전체') return portfolioProjects.length;
  return portfolioProjects.filter(p => p.category === category).length;
};

// 통계
export const getPortfolioStats = () => {
  return {
    total: portfolioProjects.length,
    featured: getFeaturedProjects().length,
    categories: {
      '기업·기관 홈페이지': getCategoryCount('기업·기관 홈페이지'),
      '교육·AI 플랫폼': getCategoryCount('교육·AI 플랫폼'),
      'SaaS·업무자동화': getCategoryCount('SaaS·업무자동화'),
      '공공·예약·문화': getCategoryCount('공공·예약·문화')
    }
  };
};

// 모든 태그 추출
export const getAllTags = () => {
  const tagSet = new Set<string>();
  portfolioProjects.forEach(project => {
    project.tags.forEach(tag => tagSet.add(tag));
  });
  return Array.from(tagSet).sort();
};
